#include "printf.h"

int	ft_prints(char *c)
{
	int	i;
	int	counter;

	i = 0;
	counter = 0;
	while (c[i] != '\0')
	{
		counter += write(1, &c[i], 1);
		i++;
	}
	return (counter);
}
